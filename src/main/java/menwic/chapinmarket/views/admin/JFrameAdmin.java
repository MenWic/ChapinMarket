    private void jMenuItemActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemActualizarActionPerformed
        String empleado = JOptionPane.showInputDialog("Usuario del Empleado a editar:");
        Empleado empleado1 = this.adminController.buscarEmpleado(empleado);

        if (empleado1 == null) {
            JOptionPane.showMessageDialog(null, "No se encontro el empeado ingresado");
        } else {
            JPanelActualizarEmpleado aEmpleado = new JPanelActualizarEmpleado(empleado1);
            aEmpleado.setSize(750, 479);
            aEmpleado.setLocation(0, 0);

            jPanelContenedor.removeAll();
            jPanelContenedor.add(aEmpleado, BorderLayout.CENTER);
            jPanelContenedor.revalidate();
            jPanelContenedor.repaint();
        }
    }//GEN-LAST:event_jMenuItemActualizarActionPerformed

